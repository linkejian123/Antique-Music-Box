void VFDdisplayMessage(char* message)
{
    int byteIndex = 0;
   
    PORTGbits.RG2 = 0;    // set blank as low
    while(*message != '\0')
    {
        byteIndex = *message - 0x20;
        shiftout(byteIndex);
        PORTFbits.RF4 = 1;
        PORTFbits.RF4 = 0;
        *message++;
        delay(100);
    }
}

void delay(int n)
{
    int i;
    int index;
    for(i=0;i<20000;i++)
    {
        for(index=0;index<n;index++);
    }
}

